    private void cargarCombos() {

        cmbEstudiante.removeAllItems();

        for (Estudiante e : estudianteNegocio.obtenerTodos()) {

            if (e.isActivo()) {

                cmbEstudiante.addItem(
                        e.getId()
                        + " - "
                        + e.getNombre()
                );
            }
        }

        cmbCurso.removeAllItems();

        for (Curso c : cursoNegocio.obtenerTodos()) {

            if (c.isActivo()) {

                cmbCurso.addItem(
                        c.getId()
                        + " - "
                        + c.getNombre()
                );
            }
        }
    }

    private void cargarTabla() {
        llenarTabla(calificacionNegocio.obtenerTodas());
    }

    private void llenarTabla(java.util.List<Calificaciones> lista) {

        DefaultTableModel modelo
                = (DefaultTableModel) tblCalificaciones.getModel();

        modelo.setRowCount(0);

        for (Calificaciones c : lista) {

            modelo.addRow(new Object[]{
                c.getId(),
                c.getEstudiante().getNombre(),
                c.getCurso().getNombre(),
                c.getNota(),
                c.getEstado(),
                c.getObservacion()
            });
        }
    }

    private void limpiarCampos() {

        txtId.setText("");
        txtNota.setText("");
        txtObservacion.setText("");

        if (cmbEstudiante.getItemCount() > 0) {
            cmbEstudiante.setSelectedIndex(0);
        }

        if (cmbCurso.getItemCount() > 0) {
            cmbCurso.setSelectedIndex(0);
        }

        modoEdicion = false;
    }

