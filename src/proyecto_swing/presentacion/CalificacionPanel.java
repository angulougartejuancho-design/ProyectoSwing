    private void btnPromediosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromediosActionPerformed
        // TODO add your handling code here:
        StringBuilder texto = new StringBuilder();

        texto.append("Promedios por curso:\n");

        for (java.util.Map.Entry<String, Double> entrada
                : calificacionNegocio.obtenerPromediosPorCurso().entrySet()) {

            texto.append("  ")
                    .append(entrada.getKey())
                    .append(": ")
                    .append(String.format("%.2f", entrada.getValue()))
                    .append("\n");
        }

        texto.append("\nPromedios por estudiante:\n");

        for (java.util.Map.Entry<String, Double> entrada
                : calificacionNegocio.obtenerPromediosPorEstudiante().entrySet()) {

            texto.append("  ")
                    .append(entrada.getKey())
                    .append(": ")
                    .append(String.format("%.2f", entrada.getValue()))
                    .append("\n");
        }

        JOptionPane.showMessageDialog(
                this,
                texto.toString(),
                "Promedios",
                JOptionPane.INFORMATION_MESSAGE
        );
    }//GEN-LAST:event_btnPromediosActionPerformed

