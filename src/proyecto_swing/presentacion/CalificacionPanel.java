    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {

            int id = Integer.parseInt(txtId.getText().trim());

            double nota = Double.parseDouble(txtNota.getText().trim());

            int indiceEstudiante = cmbEstudiante.getSelectedIndex();
            int indiceCurso = cmbCurso.getSelectedIndex();

            if (indiceEstudiante == -1 || indiceCurso == -1) {

                JOptionPane.showMessageDialog(
                        this,
                        "Debe seleccionar estudiante y curso."
                );

                return;
            }

            Estudiante estudiante
                    = estudianteNegocio.obtenerTodos()
                            .get(indiceEstudiante);

            Curso curso
                    = cursoNegocio.obtenerTodos()
                            .get(indiceCurso);

            Calificaciones calificacion = new Calificaciones(
                    id,
                    estudiante,
                    curso,
                    nota,
                    txtObservacion.getText().trim()
            );

            if (modoEdicion) {

                calificacionNegocio.editar(calificacion);

                JOptionPane.showMessageDialog(
                        this,
                        "Calificación actualizada correctamente."
                );

            } else {

                calificacionNegocio.registrar(calificacion);

                JOptionPane.showMessageDialog(
                        this,
                        "Calificación registrada correctamente."
                );
            }

            limpiarCampos();
            cargarTabla();

        } catch (NumberFormatException ex) {

            JOptionPane.showMessageDialog(
                    this,
                    "El ID y la nota deben ser valores numéricos."
            );

        } catch (NotaException | MatriculaInvalidaException ex) {

            JOptionPane.showMessageDialog(
                    this,
                    ex.getMessage()
            );

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int fila = tblCalificaciones.getSelectedRow();

        if (fila == -1) {

            JOptionPane.showMessageDialog(
                    this,
                    "Seleccione una calificación de la tabla."
            );

            return;
        }

        Calificaciones calificacion
                = calificacionNegocio.obtenerTodas().get(fila);

        txtId.setText(String.valueOf(calificacion.getId()));
        txtNota.setText(String.valueOf(calificacion.getNota()));
        txtObservacion.setText(calificacion.getObservacion());

        for (int i = 0; i < cmbEstudiante.getItemCount(); i++) {

            if (cmbEstudiante.getItemAt(i)
                    .startsWith(calificacion.getEstudiante().getId() + " - ")) {

                cmbEstudiante.setSelectedIndex(i);
                break;
            }
        }

        for (int i = 0; i < cmbCurso.getItemCount(); i++) {

            if (cmbCurso.getItemAt(i)
                    .startsWith(calificacion.getCurso().getId() + " - ")) {

                cmbCurso.setSelectedIndex(i);
                break;
            }
        }

        modoEdicion = true;
    }//GEN-LAST:event_btnEditarActionPerformed

