    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            Estudiante estudiante = new Estudiante();

            estudiante.setId(Integer.parseInt(txtID.getText().trim()));
            estudiante.setNombre(txtNombre.getText().trim());
            estudiante.setEdad(Integer.parseInt(txtEdad.getText().trim()));
            estudiante.setCarrera(cmbCarrera.getSelectedItem().toString());

            if (rbMasculino.isSelected()) {
                estudiante.setGenero("Masculino");
            } else if (rbFemenino.isSelected()) {
                estudiante.setGenero("Femenino");
            } else {
                estudiante.setGenero("");
            }

            estudiante.setActivo(chkActivo.isSelected());
            estudiante.setCorreo(txtCorreo.getText().trim());
            estudiante.setTelefono(txtTelefono.getText().trim());
            estudiante.setObservaciones(txtObservaciones.getText().trim());

            if (modoEdicion) {
                estudianteNegocio.editar(estudiante);
                JOptionPane.showMessageDialog(this, "Estudiante actualizado correctamente.");
                modoEdicion = false;
            } else {
                estudianteNegocio.registrar(estudiante);
                JOptionPane.showMessageDialog(this, "Estudiante registrado correctamente.");
            }

            limpiarCampos();
            cargarTabla();

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El ID y la edad deben ser valores numéricos.");
        } catch (DatoInvalidoException | RegistroDuplicadoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

