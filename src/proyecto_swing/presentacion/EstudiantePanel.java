    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        // TODO add your handling code here:
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un estudiante de la tabla.");
            return;
        }

        txtID.setText(jTable1.getValueAt(fila, 0).toString());
        txtNombre.setText(jTable1.getValueAt(fila, 1).toString());
        txtEdad.setText(jTable1.getValueAt(fila, 2).toString());
        cmbCarrera.setSelectedItem(jTable1.getValueAt(fila, 3).toString());

        String genero = jTable1.getValueAt(fila, 4).toString();

        if (genero.equalsIgnoreCase("Masculino")) {
            rbMasculino.setSelected(true);
        } else {
            rbFemenino.setSelected(true);
        }

        chkActivo.setSelected(jTable1.getValueAt(fila, 5).toString().equalsIgnoreCase("Sí"));

        txtCorreo.setText(jTable1.getValueAt(fila, 6).toString());
        txtTelefono.setText(jTable1.getValueAt(fila, 7).toString());

        modoEdicion = true;
    }

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {
        if (txtID.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione un estudiante para eliminar.");
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Está seguro de que desea eliminar este estudiante?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
        );

        if (opcion == JOptionPane.YES_OPTION) {
            try {
                int id = Integer.parseInt(txtID.getText().trim());

                estudianteNegocio.eliminar(id);

                JOptionPane.showMessageDialog(this, "Estudiante eliminado correctamente.");

                limpiarCampos();
                cargarTabla();
                modoEdicion = false;

            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "El ID no es válido.");
            }
        }
    }//GEN-LAST:event_EditarActionPerformed

    private void ExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExportarActionPerformed
        // TODO add your handling code here:
        JFileChooser selector = new JFileChooser();
        selector.setDialogTitle("Guardar archivo CSV");

        int opcion = selector.showSaveDialog(this);

        if (opcion == JFileChooser.APPROVE_OPTION) {
            try {
                File archivo = selector.getSelectedFile();
                String ruta = archivo.getAbsolutePath();

                if (!ruta.toLowerCase().endsWith(".csv")) {
                    ruta += ".csv";
                }

                estudianteNegocio.exportarCSV(ruta);

                JOptionPane.showMessageDialog(this, "Archivo exportado correctamente.");

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al exportar: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_ExportarActionPerformed

